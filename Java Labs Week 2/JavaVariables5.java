public class JavaVariables5
{
	public static void main(String[] args)
	{
		float number1 = 0.345f;
		float number2 = 0.235f;

		System.out.println(number1);
		System.out.println(number2);
		System.out.println("num 1 plus num 2 is: "+(number1+number2));
		System.out.println("num1 divide num 2 is: "+(number1/number2));
		System.out.println("num1 multiply num 2 is: "+(number1*number2));
		System.out.println("num1 subtract num2 is: "+(number1-number2));
	}
}