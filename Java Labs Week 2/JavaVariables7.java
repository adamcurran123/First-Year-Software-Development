public class JavaVariables7
{
	public static void main(String[] args)
	{
	float num1= 12.3f;
	float num2= 5.5f;

	System.out.println("Number 1 + Number 2 = "+(num1+num2));
	System.out.println("Number 1 - Number 2 = "+(num1-num2));
	System.out.println("Number 1 * Number 2 = "+(num1*num2));
	System.out.println("Number 1 / Number 2 = "+(num1/num2));
	System.out.println("Number 1 % Number 2 = "+(num1%num2));
	System.out.println("Number 2 - Number 1 = "+(num2-num1));
	System.out.println("Number 2 % Number 1 = "+(num2%num1));
	}
}